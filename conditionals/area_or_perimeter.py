def area_or_perimeter(len , wid):
  if len == wid: return len * len
  else: return 2 * len + 2 * wid



print (f"Resultado para length = 3 width = 3 -- Resultado = {area_or_perimeter(3,3)}")
print (f"Resultado para length = 6 width = 10 -- Resultado = {area_or_perimeter(6,10)}")
                                                                               